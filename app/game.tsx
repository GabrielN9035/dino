import Dino from "@/components/Dino";
import MovingBackground from "@/components/MovingBackground";
import Obstacle from "@/components/Obstacle";
import Score from "@/components/Score";
import { useGame } from "@/hooks/gameHook";
import { useEffect, useState } from "react";
import { Pressable, StyleSheet, View } from "react-native";
export default function GameScreen() {
  const { jump } = useGame();
  const [obstacle, setObstacles] = useState([] as any);

  function spawnObstacle() {
    setObstacles((oldValue: any) => [...oldValue, Date.now().toString()]);
  }

  function removeObstacle(id: any) {
    setObstacles((oldValue: any) =>
      oldValue.filter((obstacle: any) => obstacle !== id),
    );
  }

  useEffect(() => {
    const interval = setInterval(() => spawnObstacle(), 10000);

    return () => clearInterval(interval);
  }, []);

  return (
    <Pressable onPress={jump} style={styles.button}>
      <View style={styles.container}>
        <MovingBackground />
        <Dino />
        <Score />
        {obstacle.map((obstacle: any) => (
          <Obstacle key={obstacle} onEnd={() => removeObstacle(obstacle)} />
        ))}
      </View>
    </Pressable>
  );
}

const styles = StyleSheet.create({
  button: {
    width: "100%",
    height: "100%",
  },
  container: {
    width: "100%",
    height: "100%",
    backgroundColor: "#99D9EA",
    position: "relative",
  },
  image: {
    width: "100%",
    height: "100%",
  },
});
